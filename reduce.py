import numpy as np

class Reduce():
    def add_row(self, mat, i, j, r=1):
        mat[i, :] = (mat[i, :] + r * mat[j, :]) % 2
        return mat

    def flip_rows(self, mat, i, j):
        mat[[i,j]] = mat[[j,i]] 
        return mat

    def add_col(self, mat, i, j, r=1):
        mat[:, i] = (mat[:, i] + r * mat[:, j] ) % 2
        return mat  

    def flip_cols(self, mat, i, j):
        mat[:, [i,j]] = mat[:, [j,i]] 
        return mat

    def reduce(self, mat, i=0):
        m, n = mat.shape        
